#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*s2;

	i = 0;
	s2 = (unsigned char *)s;
	while ((i < n) && (s2[i] != c))
		i++;
	if (s2[i] == c)
		return (&s2[i]);
	else
		return (NULL);
}
